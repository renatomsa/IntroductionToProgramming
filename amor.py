frase = input()

if (frase == 'Olha a chuva!'):
    print ('Grite choveu!')
elif (frase == 'Olha a cobra!'):
    print ('Grite é mentira')
elif (frase == 'Cavalheiros ajoelhados de frente pras damas!'):
    print ('Ajoelhe-se na frente da dama, beije a mão dela e grite beijou!')
elif (frase == 'Alavantu!'):
    print ('Vá em direção a sua dama')
elif (frase == 'Anarriê!'):
    print ('Se distancie da sua dama')
elif (frase == 'Balancê'):
    print ('Dance com a sua dama')